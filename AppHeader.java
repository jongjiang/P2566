public class AppHeader {
	private int appSourceID;
	private int appDestID;
	private String appUID;
	private String appDateTime;

	// Getters and setters

	public int getAppSourceID() {
		return appSourceID;
	}

	public void setAppSourceID(int appSourceID) {
		this.appSourceID = appSourceID;
	}

	public int getAppDestID() {
		return appDestID;
	}

	public void setAppDestID(int appDestID) {
		this.appDestID = appDestID;
	}

	public String getAppUID() {
		return appUID;
	}

	public void setAppUID(String appUID) {
		this.appUID = appUID;
	}

	public String getAppDateTime() {
		return appDateTime;
	}

	public void setAppDateTime(String appDateTime) {
		this.appDateTime = appDateTime;
	}

}