public class AppRepBody {
	private String noticeData;
	private String fiscSTAN;
	private String fiscRC;
	private String responseCode;
	private String responseDesc;

	// Getters and setters

	public String getNoticeData() {
		return noticeData;
	}

	public void setNoticeData(String noticeData) {
		this.noticeData = noticeData;
	}

	public String getFiscSTAN() {
		return fiscSTAN;
	}

	public void setFiscSTAN(String fiscSTAN) {
		this.fiscSTAN = fiscSTAN;
	}

	public String getFiscRC() {
		return fiscRC;
	}

	public void setFiscRC(String fiscRC) {
		this.fiscRC = fiscRC;
	}

	public String getResponseCode() {
		return responseCode;
	}

	public void setResponseCode(String responseCode) {
		this.responseCode = responseCode;
	}

	public String getResponseDesc() {
		return responseDesc;
	}

	public void setResponseDesc(String responseDesc) {
		this.responseDesc = responseDesc;
	}

}